#include<stdio.h>
int main()
{
    int a[5],i,sum=0;

    for(i=0;i<5;i++)
    {
        printf("Enter number %d: ",i+1);
        scanf("%d",&a[i]);
        sum=sum+a[i];
    }

    printf("Sum = %d",sum);

    return 0;
}


